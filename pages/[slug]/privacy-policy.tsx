export async function getServerSideProps(context: any) {
  const { slug } = context.query;
  const headerData = await getUserDetail(slug);
  try {
    if (headerData?.data) {
      return {
        props: {
          headerData: headerData ? headerData : [],
          slug: slug || [],
        },
      };
    } else {
      return {
        props: {
          error: true,
        },
      };
    }
  } catch (error) {}
}
